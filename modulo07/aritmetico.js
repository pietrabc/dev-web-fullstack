// Adição

let valor = 10;
let segundoValor = 43;

console.log(valor + segundoValor);

// Subtração
console.log(valor - segundoValor);
console.log(segundoValor - valor);

//Multiplicação
console.log(valor * segundoValor);

// Divisão
console.log(valor / segundoValor);
console.log(segundoValor / valor);

// Módulo ( resto da divisão inteira)
console.log(segundoValor % valor);
console.log(valor % segundoValor);
