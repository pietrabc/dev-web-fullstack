let produtos = ['Carrinho', 'Boneca', 'Bola'];
// .push() ao final da lista
produtos.push('Navio', 'Corda');
console.log(produtos);
// .unshift() no início da lista
produtos.unshift('Video Game');
console.log(produtos);

// .pop remover o último elemento
produtos.pop();
console.log(produtos);
// .shift() remove o primeiro elemento
produtos.shift();
console.log(produtos);
